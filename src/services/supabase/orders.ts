            replacedObligationId = oldObligation.id;
            const isPaid = oldObligation.status === 'paid';
            const notePrefix = isPaid ? 'تسوية سالبة: ' : '';
            const reasonSuffix = input.reasonNote ? `. السبب: ${input.reasonNote}` : '';
