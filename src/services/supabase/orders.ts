export interface StatusUpdateContext {
    designUrl?: string | null;      // When designer uploads a design or clearing it
    comment?: string;        // Optional comment to add
    userId?: string;         // User making the change
    userName?: string;       // User name for comment attribution
    actorRole?: string;      // User role when available
    rejectedLabCost?: number; // Cost to lab when rejected
    rejectedDesignerCost?: number; // Cost to per-piece designer when rejected
    issueState?: Order['issueState']; // Optional issueState to set atomically with status
}

/**
 * STATUS SYNCHRONIZATION MAP
 * Defines which designStatus should accompany each main status for Split Workflows.
 */
const STATUS_TO_DESIGN_STATUS: Record<string, Order['designStatus'] | undefined> = {
    'New Case': 'pending',
    'Under Design': 'in_progress',
    'Waiting Dr Approval': 'waiting_approval',
    'Under Production': 'completed',
    'Try In': 'completed',
    'Try In Approved': 'completed',
    'Ready': 'completed',
    'Delivered': 'completed',
    'Returned for Adjustments': 'returned',
    'Doctor Rejected': undefined, // Designer not involved
    'Lab Rejected': undefined,    // Lab rejected internally
};

/**
 * Centralized function for updating order status.
 * ENFORCES:
 * - Status/designStatus synchronization for Split Workflows
 * - Status history tracking
 * - Optional side-effects (design URL, comments)
 * 
 * UI MUST NOT update status or designStatus directly. Use this function only.
 */
export async function updateOrderStatus(
    orderId: string,
    newStatus: Order['status'],
    context: StatusUpdateContext = {}
): Promise<Order | null> {
    // Validate UUID
    if (!orderId || typeof orderId !== 'string' || !/^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i.test(orderId)) {
        throw new ValidationError('معرف الطلب غير صحيح');
    }

    // Fetch current order to determine if it's a Split Workflow
    const currentOrder = await getOrder(orderId);
    if (!currentOrder) {
        throw new ValidationError('الطلب غير موجود');
    }

    // Build updates object
    const updates: Partial<Order> = {
        status: newStatus,
    };

    // Explicitly set productionStatus to assist the database trigger sync
    const currentProdStatus = getProductionStatus(currentOrder);
    const currentIssueState = getEffectiveIssueState(currentOrder);
    const deliveryType = (currentOrder.deliveryType || '').toLowerCase();
    const isTryIn = (deliveryType === 'tryin' || deliveryType === 'try_in') && currentIssueState !== 'returned';

    if (newStatus === 'Ready') {
        if (isTryIn && currentProdStatus === 'finalization') {
            updates.productionStatus = 'final_ready';
        } else if (!isTryIn) {
            updates.productionStatus = 'final_ready';
        } else {
            updates.productionStatus = 'try_in_ready';
        }
    } else if (newStatus === 'Try In Approved') {
        updates.productionStatus = 'finalization';
    } else if (newStatus === 'Try In') {
        updates.productionStatus = 'try_in_ready';
    } else if (newStatus === 'Delivered' || newStatus === 'Completed') {
        updates.productionStatus = 'final_delivered';
    } else if (['Under Production', 'In Progress', 'Returned for Adjustments'].includes(newStatus)) {
        updates.productionStatus = 'in_production';
    } else if (['Under Design', 'Waiting Dr Approval'].includes(newStatus)) {
        updates.productionStatus = 'designing';
    } else if (['New Case', 'Pending', 'Pending Review', 'Cancelled', 'Lab Rejected', 'Doctor Rejected'].includes(newStatus)) {
        updates.productionStatus = 'not_started';
    }

    // CRITICAL: Sync designStatus for Split Workflows
    if (currentOrder.workflowType === 'split') {
        const syncedDesignStatus = STATUS_TO_DESIGN_STATUS[newStatus];
        if (syncedDesignStatus !== undefined) {
            updates.designStatus = syncedDesignStatus;
        }
    }

    // Handle design URL if provided (allow explicit null to clear it)
    if (context.designUrl !== undefined) {
        updates.designUrl = context.designUrl;
    }

    // Handle comments if provided
    if (context.comment && context.userId && context.userName) {
        const newComment = {
            id: Math.random().toString(36).substr(2, 9),
            text: context.comment,
            userId: context.userId,
            userName: context.userName,
            createdAt: new Date().toISOString()
        };
        updates.comments = [...(currentOrder.comments || []), newComment];
    }

    if (context.rejectedLabCost !== undefined) {
        updates.rejectedLabCost = context.rejectedLabCost;
    }
    if (context.rejectedDesignerCost !== undefined) {
        updates.rejectedDesignerCost = context.rejectedDesignerCost;
    }

    if (context.issueState !== undefined) {
        updates.issueState = context.issueState;
    }

    // Use existing updateOrder for the actual update (handles history tracking)
    const updatedOrder = await updateOrder(orderId, updates, {
        ...context,
        allowStatusChange: true,
    });

    const wasDelivered = getProductionStatus(currentOrder) === 'final_delivered';
    const isNowDelivered = updatedOrder ? getProductionStatus(updatedOrder) === 'final_delivered' : false;
    const isDeliveredReversal = updatedOrder ? wasDelivered && !isNowDelivered : false;

    if (updatedOrder && currentOrder.status !== newStatus) {
        const eventType = isDeliveredReversal
            ? ORDER_EVENT_TYPES.deliveryReverted
            : getStatusChangeEventType(newStatus);
        const deliveredAt = updatedOrder.actualDeliveryDate || new Date().toISOString().split('T')[0];
        const autoMarkedReady = isNowDelivered && !wasDelivered && !isFinalReady(currentOrder);

        await createOrderEventNonBlocking(eventType, {
            orderId,
            eventType,
            oldValue: currentOrder.status,
            newValue: newStatus,
            changedBy: context.userId || null,
            actorRole: context.actorRole || null,
            severity: isDeliveredReversal ? 'warning' : 'info',
            metadata: {
                legacyStatus: newStatus,
                previousLegacyStatus: currentOrder.status,
                productionStatus: getProductionStatus(updatedOrder),
                previousProductionStatus: getProductionStatus(currentOrder),
                wfProductionStatus: (updatedOrder as unknown as Record<string, unknown>).productionStatus || null,
                wfIssueState: (updatedOrder as unknown as Record<string, unknown>).issueState || null,
                previousWfProductionStatus: (currentOrder as unknown as Record<string, unknown>).productionStatus || null,
                previousWfIssueState: (currentOrder as unknown as Record<string, unknown>).issueState || null,
                designStatus: updatedOrder.designStatus || null,
                previousDesignStatus: currentOrder.designStatus || null,
                ...(isNowDelivered && autoMarkedReady ? {
                    autoMarkedReady: true,
                    readyAt: deliveredAt,
                    impliedFinalReady: true,
                } : {}),
                ...(isDeliveredReversal ? {
                    previousActualDeliveryDate: currentOrder.actualDeliveryDate || null,
                    revertedToStatus: newStatus,
                    revertedAt: new Date().toISOString(),
                    shadowMode: true,
                    shadowObligationVoidNeeded: true,
                } : {}),
            },
        });
    }

    if (
        updatedOrder
        && isDeliveredReversal
        && !shouldVoidDoctorReceivableForStatusOrIssueChange(currentOrder, updatedOrder)
    ) {
        const voidReason = `Order reverted from Delivered to ${newStatus}`;
        try {
            const obligation = await findActiveDoctorDeliveredObligationForOrder(orderId);

            if (obligation) {
                await voidFinancialObligation(obligation.id, voidReason, {
                    voidReason: 'delivery_reverted',
                    revertedToStatus: newStatus,
                    voidedAt: new Date().toISOString(),
                    shadowMode: true,
                });
                await reallocatePaymentsAfterObligationVoid(obligation.id, null, context.userId || null);
            } else {
                console.debug('No active shadow doctor receivable obligation found to void after Delivered reversal', {
                    orderId,
                    newStatus,
                });
            }
        } catch (error) {
            console.error(DOCTOR_RECEIVABLE_OBLIGATION_VOID_FAILURE_MESSAGE, {
                orderId,
                newStatus,
                error,
            });
            throw new Error(DOCTOR_RECEIVABLE_OBLIGATION_VOID_FAILURE_MESSAGE);
        }
    }

    if (
        updatedOrder
        && FINANCIAL_OBLIGATIONS_FLAGS.trackingEnabled
        && shouldVoidExternalLabReadyObligationForStatusChange(currentOrder, updatedOrder)
    ) {
        const notes = 'Order left Final Ready/Delivered workflow; normal external lab payable voided. Issue settlement may be entered separately.';

        try {
            const obligation = await findActiveExternalLabReadyObligationForOrder(orderId);

            if (obligation) {
                await voidFinancialObligation(obligation.id, notes, {
                    voidReason: 'left_final_ready_or_issue_status',
                    previousStatus: currentOrder.status,
                    newStatus,
                    shadowMode: true,
                    reviewNeeded: true,
                    issueSettlementMayBeNeeded: true,
                });
                await reallocatePaymentsAfterObligationVoid(obligation.id, null, context.userId || null);
            } else {
                console.debug('No active shadow external lab ready obligation found to void after leaving Final Ready/Delivered workflow', {
                    orderId,
                    previousStatus: currentOrder.status,
                    newStatus,
                });
            }
        } catch (error) {
            console.error(EXTERNAL_LAB_PAYABLE_VOID_FAILURE_MESSAGE, {
                orderId,
                previousStatus: currentOrder.status,
                newStatus,
                error,
            });
            throw new Error(EXTERNAL_LAB_PAYABLE_VOID_FAILURE_MESSAGE);
        }
    }

    if (
        updatedOrder
        && FINANCIAL_OBLIGATIONS_FLAGS.trackingEnabled
        && shouldCreateDoctorReceivableObligationForStatusChange(currentOrder.status, newStatus)
    ) {
        try {
            await createDoctorReceivableObligationForOrder(updatedOrder, {
                createdBy: context.userId || null,
            });
        } catch (error) {
            console.error(DOCTOR_RECEIVABLE_OBLIGATION_FAILURE_MESSAGE, {
                orderId,
                status: newStatus,
                error,
            });
            throw new Error(DOCTOR_RECEIVABLE_OBLIGATION_FAILURE_MESSAGE);
        }
    }

    if (updatedOrder && FINANCIAL_OBLIGATIONS_FLAGS.trackingEnabled) {
        const externalLabPayableDecision = shouldCreateExternalLabPayableObligationForStatusChange(currentOrder, updatedOrder);

        if (externalLabPayableDecision.shouldCreate && updatedOrder.supplierId && (updatedOrder.cost || 0) > 0) {
            try {
                await createExternalLabPayableObligationForOrder(updatedOrder, {
                    createdBy: context.userId || null,
                    impliedFinalReady: externalLabPayableDecision.impliedFinalReady,
                    triggerDate: externalLabPayableDecision.impliedFinalReady
                        ? (updatedOrder.actualDeliveryDate || new Date().toISOString().split('T')[0])
                        : undefined,
                });
            } catch (error) {
                console.error(EXTERNAL_LAB_PAYABLE_OBLIGATION_FAILURE_MESSAGE, {
                    orderId,
                    status: newStatus,
                    supplierId: updatedOrder.supplierId,
                    cost: updatedOrder.cost,
                    impliedFinalReady: externalLabPayableDecision.impliedFinalReady,
                    error,
                });
                throw new Error(EXTERNAL_LAB_PAYABLE_OBLIGATION_FAILURE_MESSAGE);
            }
        }
    }

    // Designer payable: void when design is reverted, create when it completes
    if (updatedOrder && FINANCIAL_OBLIGATIONS_FLAGS.trackingEnabled) {
        if (shouldVoidDesignerPayableObligationForDesignStatusChange(currentOrder, updatedOrder)) {
            try {
                const obligation = await findActiveDesignerApprovedObligationForOrder(orderId);
                if (obligation) {
                    await voidFinancialObligation(obligation.id, `Design status reverted to ${updatedOrder.designStatus}`, {
                        voidReason: 'design_status_reverted',
                        previousDesignStatus: currentOrder.designStatus || null,
                        newDesignStatus: updatedOrder.designStatus || null,
                        shadowMode: true,
                    });
                    await reallocatePaymentsAfterObligationVoid(obligation.id, null, context.userId || null);
                }
            } catch (error) {
                console.error(DESIGNER_PAYABLE_OBLIGATION_VOID_FAILURE_MESSAGE, { orderId, error });
                throw new Error(DESIGNER_PAYABLE_OBLIGATION_VOID_FAILURE_MESSAGE);
            }
        } else if (shouldCreateDesignerPayableObligationForDesignStatusChange(currentOrder, updatedOrder)) {
            try {
                await createDesignerPayableObligationForOrder(updatedOrder, {
                    createdBy: context.userId || null,
                });
            } catch (error) {
                console.error(DESIGNER_PAYABLE_OBLIGATION_FAILURE_MESSAGE, { orderId, error });
                throw new Error(DESIGNER_PAYABLE_OBLIGATION_FAILURE_MESSAGE);
            }
        }
    }

    return updatedOrder;
}

