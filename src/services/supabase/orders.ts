async function correctExternalLabPayableAmountAfterOrderUpdate(input: {
    previousOrder: Order;
    updatedOrder: Order;
    changedBy?: string | null;
    reasonCode?: string | null;
    reasonNote?: string | null;
}): Promise<void> {
    const impliedFinalReady = getProductionStatus(input.updatedOrder) === 'final_delivered' && !isTryInOrder(input.updatedOrder);
    const isPayableEligible = (isFinalReady(input.updatedOrder) || impliedFinalReady) && !!input.updatedOrder.supplierId;
    if (!isPayableEligible) return;

    let isUpdatedDesignerSalaried = false;
    if (input.updatedOrder.designerId) {
        const { data: u } = await supabase
            .from('users')
            .select('custom_permissions')
            .eq('id', input.updatedOrder.designerId)
            .maybeSingle();
        isUpdatedDesignerSalaried = Boolean(u?.custom_permissions?.['designer_fixed_salary']);
    }

    const labCostMetadata = getLabCostMetadata(input.updatedOrder, isUpdatedDesignerSalaried);
    const newAmount = labCostMetadata.cost;
    const activeObligation = await findActiveExternalLabReadyObligationForOrder(input.updatedOrder.id, input.updatedOrder.supplierId);

    let isPreviousDesignerSalaried = false;
    if (input.previousOrder.designerId) {
        const { data: u } = await supabase
            .from('users')
            .select('custom_permissions')
            .eq('id', input.previousOrder.designerId)
            .maybeSingle();
        isPreviousDesignerSalaried = Boolean(u?.custom_permissions?.['designer_fixed_salary']);
    }

    const previousAmount = activeObligation?.grossAmount ?? getLabCostMetadata(input.previousOrder, isPreviousDesignerSalaried).cost;

    if (newAmount <= 0) {
        if (activeObligation) {
            const isPaid = activeObligation.status === 'paid';
            const notePrefix = isPaid ? 'تسوية سالبة: ' : '';
            const reasonSuffix = input.reasonNote ? `. السبب: ${input.reasonNote}` : '';
            await voidFinancialObligation(
                activeObligation.id,
                `${notePrefix}External lab payable amount corrected from ${activeObligation.grossAmount} to 0${reasonSuffix}`,
                {
                    voidReason: 'lab_cost_zero_or_removed',
                    previousAmount: activeObligation.grossAmount,
                    newAmount: 0,
                    changedBy: input.changedBy || null,
                    reasonCode: input.reasonCode || null,
                    reasonNote: input.reasonNote || null,
                    shadowMode: true,
                    isNegativeAdjustment: isPaid,
                }
            );
            await reallocatePaymentsAfterObligationVoid(activeObligation.id, null, input.changedBy || null);
        }
        return;
    }

    const createContext = {
        createdBy: input.changedBy || null,
        impliedFinalReady,
        triggerDate: impliedFinalReady
            ? (input.updatedOrder.actualDeliveryDate || new Date().toISOString().split('T')[0])
            : undefined,
    };

    if (!activeObligation) {
        await createExternalLabPayableObligationForOrder(input.updatedOrder, {
            ...createContext,
            metadata: {
                correctionReason: 'lab_cost_added_after_ready_or_delivered',
                previousAmount,
                newAmount,
                ...labCostMetadata,
                changedBy: input.changedBy || null,
                reasonCode: input.reasonCode || null,
                reasonNote: input.reasonNote || null,
                shadowMode: true,
                trackingOnly: true,
            },
        });
        return;
    }

    if (activeObligation.grossAmount === newAmount) return;

    const isPaid = activeObligation.status === 'paid';
    const notePrefix = isPaid && (newAmount < activeObligation.grossAmount) ? 'تسوية سالبة: ' : '';
    const reasonSuffix = input.reasonNote ? `. السبب: ${input.reasonNote}` : '';
    await voidFinancialObligation(
        activeObligation.id,
        `${notePrefix}External lab payable amount corrected from ${activeObligation.grossAmount} to ${newAmount}${reasonSuffix}`,
        {
            voidReason: 'lab_cost_corrected',
            previousAmount: activeObligation.grossAmount,
            newAmount,
            changedBy: input.changedBy || null,
            reasonCode: input.reasonCode || null,
            reasonNote: input.reasonNote || null,
            shadowMode: true,
            isNegativeAdjustment: isPaid && (newAmount < activeObligation.grossAmount),
        }
    );

    const newOblig = await createExternalLabPayableObligationForOrder(input.updatedOrder, {
        ...createContext,
        metadata: {
            correctionReason: 'lab_cost_corrected',
            previousAmount: activeObligation.grossAmount,
            newAmount,
            replacedObligationId: activeObligation.id,
            ...labCostMetadata,
            changedBy: input.changedBy || null,
            reasonCode: input.reasonCode || null,
            reasonNote: input.reasonNote || null,
            shadowMode: true,
            trackingOnly: true,
        },
    });

    await reallocatePaymentsAfterObligationVoid(
        activeObligation.id,
        newOblig ? newOblig.id : null,
        input.changedBy || null
    );

}

